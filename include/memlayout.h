#ifndef __MEMLAYOUT_H__
#define __MEMLAYOUT_H__

#include <types.h>
#include <param.h>

/*===========================================================================
 * WinixOS 64位内存布局
 *
 * x86-64 虚拟地址空间（48位可用，两个规范区域）：
 *
 *   0x0000_0000_0000_0000 ~ 0x0000_7FFF_FFFF_FFFF  用户空间 (128TB)
 *     0x0000_0000_0040_0000   用户程序加载起始地址 (ELF 默认)
 *     用户堆向上增长
 *     用户栈从 USERTOP 向下增长
 *
 *   规范空洞（non-canonical，访问触发 #GP）
 *
 *   0xFFFF_8000_0000_0000 ~ 0xFFFF_FFFF_FFFF_FFFF  内核空间 (128TB)
 *     KERNBASE = 0xFFFF_8000_0000_0000
 *     物理内存直接映射区：PA 0 ~ PHYSTOP 对应 KERNBASE ~ KERNBASE+PHYSTOP
 *     内核代码/数据：KERNBASE + 0x10_0000（物理 1MB 处加载）
 *     每进程内核栈：由 kalloc() 分配，通过 P2V 映射到高地址
 *===========================================================================*/

/*---------------------------------------------------------------------------
 * 内核虚拟地址基址（高半规范地址区的起始，512GB 对齐）
 * 物理内存在此基址上进行直接映射：
 *   虚拟地址 KERNBASE + pa  ↔  物理地址 pa
 *---------------------------------------------------------------------------*/
#define KERNBASE         0xFFFF800000000000ULL

/*---------------------------------------------------------------------------
 * 虚拟地址 ↔ 物理地址转换宏
 *   V2P(va)：内核虚拟地址 → 物理地址（减去 KERNBASE 偏移）
 *   P2V(pa)：物理地址 → 内核虚拟地址（加上 KERNBASE 偏移）
 *---------------------------------------------------------------------------*/
#define V2P(va)          ((uint64_t)(va) - KERNBASE)
#define P2V(pa)          ((void *)((uint64_t)(pa) + KERNBASE))

/* 常量版本（用于汇编或无类型转换场景）*/
#define V2P_C(va)        ((va) - KERNBASE)
#define P2V_C(pa)        ((pa) + KERNBASE)

/*---------------------------------------------------------------------------
 * 用户地址空间边界
 *   USERTOP：用户地址空间顶部（即规范空洞开始处）
 *   USTACK  ：用户栈顶（exec 时从此向下分配，同 USERTOP）
 *---------------------------------------------------------------------------*/
#define USERTOP          0x0000800000000000ULL
#define USTACK           USERTOP

/*---------------------------------------------------------------------------
 * 物理内存
 *   KERN_PHYS_BASE：内核被 bootloader 加载到的物理地址（1MB 处）
 *   PHYSTOP       ：物理内存上限（默认 2GB，实际由 E820 确定）
 *---------------------------------------------------------------------------*/
#define KERN_PHYS_BASE   0x100000ULL
#define PHYSTOP          0x80000000ULL    /* 2GB */

/*---------------------------------------------------------------------------
 * 内核镜像结束地址（由 linker/kernel.ld 导出的链接符号）
 * 注意：链接到高地址后，__kernel_end 也是高地址虚拟地址
 *---------------------------------------------------------------------------*/
extern char __kernel_end[];

/*---------------------------------------------------------------------------
 * 页对齐宏（依赖 param.h 中的 PGSIZE）
 *---------------------------------------------------------------------------*/
#define PGROUNDUP(x)     ALIGN_UP((uintptr_t)(x), PGSIZE)
#define PGROUNDDOWN(x)   ALIGN_DOWN((uintptr_t)(x), PGSIZE)

#endif /* __MEMLAYOUT_H__ */