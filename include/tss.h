#ifndef __TSS_H__
#define __TSS_H__

/*===========================================================================
 * include/tss.h — x86-64 任务状态段（TSS）结构定义
 *
 * x86-64 中 TSS 的主要作用已从 32 位的任务切换变为：
 *   1. 提供 RSP0：当从用户态（CPL=3）陷入内核（CPL=0）时，
 *      处理器从 TSS.RSP0 字段加载内核栈顶地址。
 *   2. IST（Interrupt Stack Table）：为特定中断向量提供固定内核栈，
 *      防止双重故障时栈不可用。
 *
 * 每个 CPU 核心对应一个 TSS，存储在各自的 CPU 结构中。
 *
 * 参考：Intel SDM Vol.3 Section 7.2.1（64-Bit Mode TSS）
 *===========================================================================*/

#include <types.h>

/*---------------------------------------------------------------------------
 * struct tss64 — 64 位任务状态段
 *
 * 布局固定，由 Intel 硬件规范定义，共 104 字节（不含 IOPB）。
 * 必须 4 字节对齐；实际上我们在 GDT 中注册时需要 16 字节对齐的描述符。
 *---------------------------------------------------------------------------*/
struct tss64 {
    uint32_t  reserved0;         /* 保留，必须为 0 */
    uint64_t  rsp0;              /* 内核栈顶（CPL=3→CPL=0 时使用）*/
    uint64_t  rsp1;              /* CPL=1 栈顶（通常不用）*/
    uint64_t  rsp2;              /* CPL=2 栈顶（通常不用）*/
    uint64_t  reserved1;         /* 保留 */
    uint64_t  ist[7];            /* IST[1..7]：中断栈表（ist[0] 对应 IST #1）*/
    uint64_t  reserved2;         /* 保留 */
    uint16_t  reserved3;         /* 保留 */
    uint16_t  iomap_base;        /* I/O 许可位图偏移（设为 sizeof(tss64) 表示无 IOPB）*/
} __attribute__((packed));

/*---------------------------------------------------------------------------
 * 64 位 TSS 描述符（系统段描述符）
 *
 * 与 32 位不同，64 位 TSS 描述符占 **16 字节**（两个 GDT slot），
 * 其中高 8 字节存放基址的高 32 位（bits 63:32）。
 *
 * 低 8 字节（标准形式）：
 *   [15:0]   段限制低 16 位
 *   [39:16]  基址低 24 位
 *   [43:40]  类型（0x9 = 64-bit TSS available）
 *   [44]     S=0（系统段）
 *   [46:45]  DPL
 *   [47]     P（Present）
 *   [51:48]  段限制高 4 位
 *   [52]     AVL
 *   [54:53]  保留（64 位模式 L/DB=0）
 *   [55]     G（Granularity）
 *   [63:56]  基址 bits 31:24
 *
 * 高 8 字节：
 *   [31:0]   基址 bits 63:32
 *   [63:32]  保留（必须为 0）
 *---------------------------------------------------------------------------*/
struct tss64_desc {
    uint64_t low;    /* TSS 描述符低64位 */
    uint64_t high;   /* TSS 描述符高64位（基址高32位）*/
} __attribute__((packed));

/*---------------------------------------------------------------------------
 * TSS 管理函数
 *---------------------------------------------------------------------------*/

/*
 * tss_init — 初始化 BSP 的 TSS 并将其注册到 GDT
 *
 * 必须在 idt_init() 之后、进程调度启动之前调用。
 * 将 TSS 描述符写入 GDT[TSS_SEL/8] 和 GDT[TSS_SEL/8 + 1]，
 * 然后执行 ltr 指令加载 TSS 段寄存器。
 */
void tss_init(void);

/*
 * tss_set_rsp0 — 设置当前 CPU 的 TSS.RSP0
 *
 * 在每次进程切换时调用，将新进程的内核栈顶写入 TSS.RSP0，
 * 使得该进程从用户态陷入内核时能正确切换到其内核栈。
 *
 * @rsp0: 新进程内核栈顶虚拟地址（kstack + KSTACKSIZE）
 */
void tss_set_rsp0(uint64_t rsp0);

#endif /* __TSS_H__ */
