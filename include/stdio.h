#ifndef __STDIO_H__
#define __STDIO_H__

/*===========================================================================
 * stdio.h — 内核格式化输出接口
 *
 * 提供类 printf 的格式化输出功能，输出目标为:
 *   1. VESA 帧缓冲屏幕（通过 video_print）
 *   2. QEMU 调试端口 0xE9（通过 outb，在 QEMU 终端可见）
 *
 * 支持的格式说明符:
 *   %d  / %ld  — 有符号十进制整数 (int / int64_t)
 *   %u  / %lu  — 无符号十进制整数 (unsigned int / uint64_t)
 *   %x  / %lx  — 十六进制小写 (unsigned int / uint64_t)
 *   %X  / %lX  — 十六进制大写
 *   %p         — 指针，输出格式 0xXXXXXXXXXXXXXXXX
 *   %s         — 字符串 (NULL 安全，显示 "(null)")
 *   %c         — 单个字符
 *   %%         — 输出百分号
 *
 * 注意: 不支持宽度/精度修饰符（如 %08x），内核中通常不需要。
 *===========================================================================*/

#include <types.h>
#include <stdarg.h>

/*---------------------------------------------------------------------------
 * kprintf — 内核格式化输出（白色文字）
 *
 * 将格式化结果输出到屏幕（白色）和 QEMU 调试端口。
 * 内部使用 256 字节栈缓冲区，单次输出不应超过此长度。
 *
 * @fmt: printf 风格的格式字符串
 * @...: 可变参数列表
 *---------------------------------------------------------------------------*/
void kprintf(const char *fmt, ...);

/*---------------------------------------------------------------------------
 * kprintf_color — 带颜色的格式化输出
 *
 * 与 kprintf 相同，但允许指定前景色。
 * 颜色常量定义在 video.h（COLOR_WHITE, COLOR_CYAN, COLOR_GREEN 等）。
 *
 * @fg:  前景色（32bpp RGB 值，格式 0x00RRGGBB）
 * @fmt: printf 风格的格式字符串
 * @...: 可变参数列表
 *---------------------------------------------------------------------------*/
void kprintf_color(uint32_t fg, const char *fmt, ...);

/*---------------------------------------------------------------------------
 * sprintf — 格式化输出到字符串缓冲区
 *
 * 将格式化结果写入 buf，不限制长度（由调用者保证缓冲区足够大）。
 * 建议优先使用 snprintf。
 *
 * @buf: 目标字符串缓冲区
 * @fmt: printf 风格的格式字符串
 * @...: 可变参数列表
 * 返回: 写入的字符数（不含末尾 '\0'）
 *---------------------------------------------------------------------------*/
int sprintf(char *buf, const char *fmt, ...);

/*---------------------------------------------------------------------------
 * snprintf — 安全格式化输出到字符串缓冲区
 *
 * 最多向 buf 写入 size-1 个字符，并在末尾添加 '\0'。
 * 安全版本，防止缓冲区溢出。
 *
 * @buf:  目标字符串缓冲区
 * @size: 缓冲区大小（字节），输出被截断到 size-1 个字符
 * @fmt:  printf 风格的格式字符串
 * @...:  可变参数列表
 * 返回: 实际写入的字符数（不含末尾 '\0'，可能小于 size-1）
 *---------------------------------------------------------------------------*/
int snprintf(char *buf, size_t size, const char *fmt, ...);

/*---------------------------------------------------------------------------
 * vsnprintf — 格式化输出核心（va_list 版本）
 *
 * kprintf/sprintf/snprintf 的底层实现，接受已初始化的 va_list。
 * 可用于实现自定义封装函数（参考 kprintf_color 的实现）。
 *
 * @buf:  目标字符串缓冲区
 * @size: 缓冲区大小（字节）
 * @fmt:  printf 风格的格式字符串
 * @ap:   已由 va_start 初始化的参数列表
 * 返回: 实际写入的字符数（不含末尾 '\0'）
 *---------------------------------------------------------------------------*/
int vsnprintf(char *buf, size_t size, const char *fmt, va_list ap);

#endif /* __STDIO_H__ */
