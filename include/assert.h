#ifndef __ASSERT_H__
#define __ASSERT_H__

/*===========================================================================
 * assert.h — 内核断言宏
 *
 * 提供运行时条件检查。断言失败时调用 panic()，输出失败信息并永久停机。
 * 与用户空间断言不同，内核断言失败是不可恢复的致命错误。
 *
 * 使用方法:
 *   assert(ptr != NULL);            // 条件必须为真
 *   assert(size >= sizeof(int));    // 不满足则触发 panic
 *   assert(kalloc() != NULL);       // 验证内存分配成功
 *
 * 失败时输出示例:
 *   PANIC: assertion failed: ptr != NULL
 *   System halted.
 *
 * 注意: 内核断言不可禁用（无 NDEBUG 支持），始终有效。
 *===========================================================================*/

#include <types.h>

/*---------------------------------------------------------------------------
 * assert — 运行时断言宏
 *
 * @cond: 期望为真的布尔表达式。
 *        若 cond 为假（0），则调用 panic() 并打印失败的条件字符串。
 *
 * 使用 do { } while(0) 包裹，使宏在 if/else 中行为正确:
 *   if (x) assert(y);  // 等价于 if (x) { assert(y); }
 *---------------------------------------------------------------------------*/
#define assert(cond) \
    do { if (!(cond)) panic("assertion failed: " #cond); } while(0)

/*---------------------------------------------------------------------------
 * panic — 内核致命错误处理（实现在 kernel/panic.c）
 *
 * 打印错误信息到屏幕，然后关闭中断并永久执行 hlt 指令。
 * @msg: 描述错误原因的字符串
 *---------------------------------------------------------------------------*/
void panic(const char *msg);

#endif /* __ASSERT_H__ */
