#ifndef __TYPES_H__
#define __TYPES_H__

/*===========================================================================
 * types.h — 内核基础类型定义
 *
 * 在裸机（freestanding）环境下，不能包含标准 C 库的 <stdint.h>，
 * 因此在此手工定义所有常用整数类型，并绑定到具体的编译器基础类型。
 * 目标架构: x86-64 (LP64 数据模型)
 *   int = 32 位, long = 64 位, pointer = 64 位
 *===========================================================================*/

/*---------------------------------------------------------------------------
 * 无符号整数类型
 * 使用 "unsigned long long" 而非 "unsigned long" 表示 64 位，
 * 确保在 Windows/MSVC ABI（LLP64: long=32 位）下也能正确编译。
 *---------------------------------------------------------------------------*/
typedef unsigned char       uint8_t;   /* 8 位无符号:  0 ~ 255 */
typedef unsigned short      uint16_t;  /* 16位无符号:  0 ~ 65535 */
typedef unsigned int        uint32_t;  /* 32位无符号:  0 ~ 4294967295 */
typedef unsigned long long  uint64_t;  /* 64位无符号:  0 ~ 18446744073709551615 */

/*---------------------------------------------------------------------------
 * 有符号整数类型
 *---------------------------------------------------------------------------*/
typedef signed char         int8_t;    /* 8 位有符号: -128 ~ 127 */
typedef signed short        int16_t;   /* 16位有符号: -32768 ~ 32767 */
typedef signed int          int32_t;   /* 32位有符号: -2147483648 ~ 2147483647 */
typedef signed long long    int64_t;   /* 64位有符号: -(2^63) ~ (2^63)-1 */

/*---------------------------------------------------------------------------
 * 指针与大小类型 (x86-64: 均为 64 位)
 *
 * uintptr_t / intptr_t: 可无损存放指针值的整数类型，用于指针算术
 * size_t:   内存大小、数组索引，与 sizeof 返回值匹配
 * ssize_t:  带符号的大小类型，用于返回可能为负（表示错误）的大小
 * off_t:    文件偏移量（64 位支持 >4GB 文件）
 *---------------------------------------------------------------------------*/
typedef uint64_t            uintptr_t; /* 无符号指针宽整数 */
typedef int64_t             intptr_t;  /* 有符号指针宽整数 */
typedef uint64_t            size_t;    /* 无符号大小类型 */
typedef int64_t             ssize_t;   /* 有符号大小类型 */
typedef int64_t             off_t;     /* 文件偏移量 */

/*---------------------------------------------------------------------------
 * 页表条目类型 (x86-64 4 级分页, 每条目 64 位)
 *
 * pte_t: 页表条目 (Page Table Entry)
 * pde_t: 页目录条目 (Page Directory Entry)
 *---------------------------------------------------------------------------*/
typedef uint64_t            pte_t;     /* 4 KB 页表条目 */
typedef uint64_t            pde_t;     /* 页目录条目 */

/*---------------------------------------------------------------------------
 * 布尔类型 (内核自定义，不依赖 <stdbool.h>)
 *---------------------------------------------------------------------------*/
typedef uint8_t             bool;      /* 0 = false, 非0 = true */
#define true  1
#define false 0

/*---------------------------------------------------------------------------
 * 空指针常量
 *---------------------------------------------------------------------------*/
#ifndef NULL
#define NULL ((void*)0)
#endif

/*---------------------------------------------------------------------------
 * 通用数学宏
 *---------------------------------------------------------------------------*/
#define MIN(a, b)    ((a) < (b) ? (a) : (b))  /* 取二者较小值 */
#define MAX(a, b)    ((a) > (b) ? (a) : (b))  /* 取二者较大值 */

/*---------------------------------------------------------------------------
 * 对齐宏
 *
 * ALIGN_UP(x, a):   将 x 向上对齐到 a（a 必须是 2 的幂）
 * ALIGN_DOWN(x, a): 将 x 向下对齐到 a（a 必须是 2 的幂）
 * IS_ALIGNED(x, a): 检查 x 是否已对齐到 a
 *
 * 示例: ALIGN_UP(0x1003, 0x1000) == 0x2000
 *       ALIGN_DOWN(0x1FFF, 0x1000) == 0x1000
 *---------------------------------------------------------------------------*/
#define ALIGN_UP(x, a)     (((x) + (a) - 1) & ~((a) - 1))
#define ALIGN_DOWN(x, a)   ((x) & ~((a) - 1))
#define IS_ALIGNED(x, a)   (((x) & ((a) - 1)) == 0)

/*---------------------------------------------------------------------------
 * 数组元素计数宏
 * 用法: NELEM(my_array) 返回 my_array 的元素个数
 * 注意: 仅适用于静态声明的数组，不能用于指针
 *---------------------------------------------------------------------------*/
#define NELEM(arr)  ((sizeof(arr)) / (sizeof((arr)[0])))

#endif /* __TYPES_H__ */
