#include <types.h>
#include <string.h>
#include <x86_64.h>

/*---------------------------------------------------------------------------
 * memset — 64位优化版内存填充
 * 对齐到8字节时使用 rep stosq (一次8字节)
 * 对齐到4字节时使用 rep stosl (一次4字节)
 * 否则逐字节 stosb
 *---------------------------------------------------------------------------*/
void *memset(void *dst, int c, size_t n)
{
    if (((uintptr_t)dst & 7) == 0 && (n & 7) == 0) {
        /* 8字节对齐: stosq */
        uint64_t val = (uint8_t)c;
        val |= val << 8;
        val |= val << 16;
        val |= val << 32;
        stosq(dst, val, n / 8);
    } else if (((uintptr_t)dst & 3) == 0 && (n & 3) == 0) {
        /* 4字节对齐: stosl */
        uint32_t val = (uint8_t)c;
        val |= val << 8;
        val |= val << 16;
        stosl(dst, val, n / 4);
    } else {
        /* 逐字节 */
        stosb(dst, c, n);
    }
    return dst;
}

/*---------------------------------------------------------------------------
 * memcmp — 逐字节比较
 *---------------------------------------------------------------------------*/
int memcmp(const void *v1, const void *v2, size_t n)
{
    const uint8_t *s1 = (const uint8_t *)v1;
    const uint8_t *s2 = (const uint8_t *)v2;

    while (n-- > 0) {
        if (*s1 != *s2)
            return *s1 - *s2;
        s1++;
        s2++;
    }
    return 0;
}

/*---------------------------------------------------------------------------
 * memmove — 内存移动，处理重叠情况
 * 参考 xv6 实现，适配64位
 *---------------------------------------------------------------------------*/
void *memmove(void *dst, const void *src, size_t n)
{
    const char *s = (const char *)src;
    char       *d = (char *)dst;

    if (s < d && s + n > d) {
        /* 重叠且目标在源之后: 从后向前复制 */
        s += n;
        d += n;
        while (n-- > 0)
            *--d = *--s;
    } else {
        /* 无重叠或目标在源之前: 从前向后复制 */
        /* 尝试64位优化 */
        if (((uintptr_t)d & 7) == 0 && ((uintptr_t)s & 7) == 0 && (n & 7) == 0) {
            movsq(d, s, n / 8);
        } else if (((uintptr_t)d & 3) == 0 && ((uintptr_t)s & 3) == 0 && (n & 3) == 0) {
            movsl(d, s, n / 4);
        } else {
            while (n-- > 0)
                *d++ = *s++;
        }
    }
    return dst;
}

/*---------------------------------------------------------------------------
 * memcpy — 内存复制 (调用 memmove，与 xv6 一致)
 *---------------------------------------------------------------------------*/
void *memcpy(void *dst, const void *src, size_t n)
{
    return memmove(dst, src, n);
}

/*---------------------------------------------------------------------------
 * memchr — 在内存中查找字符
 *---------------------------------------------------------------------------*/
void *memchr(const void *s, int c, size_t n)
{
    const uint8_t *p = (const uint8_t *)s;
    while (n-- > 0) {
        if (*p == (uint8_t)c)
            return (void *)p;
        p++;
    }
    return NULL;
}

/*---------------------------------------------------------------------------
 * strlen — 字符串长度
 *---------------------------------------------------------------------------*/
size_t strlen(const char *s)
{
    size_t n = 0;
    while (s[n])
        n++;
    return n;
}

/*---------------------------------------------------------------------------
 * strcmp — 字符串比较
 *---------------------------------------------------------------------------*/
int strcmp(const char *a, const char *b)
{
    while (*a && *a == *b) {
        a++;
        b++;
    }
    return (uint8_t)*a - (uint8_t)*b;
}

/*---------------------------------------------------------------------------
 * strncmp — 字符串前n字节比较
 *---------------------------------------------------------------------------*/
int strncmp(const char *a, const char *b, size_t n)
{
    while (n > 0 && *a && *a == *b) {
        n--;
        a++;
        b++;
    }
    if (n == 0)
        return 0;
    return (uint8_t)*a - (uint8_t)*b;
}

/*---------------------------------------------------------------------------
 * strcpy — 字符串复制
 *---------------------------------------------------------------------------*/
char *strcpy(char *dst, const char *src)
{
    char *d = dst;
    while ((*d++ = *src++) != '\0')
        ;
    return dst;
}

/*---------------------------------------------------------------------------
 * strncpy — 字符串前n字节复制
 *---------------------------------------------------------------------------*/
char *strncpy(char *dst, const char *src, size_t n)
{
    char *d = dst;
    while (n-- > 0 && (*d++ = *src++) != '\0')
        ;
    while (n-- > 0)
        *d++ = '\0';
    return dst;
}

/*---------------------------------------------------------------------------
 * safestrcpy — 安全字符串复制 (保证NUL终止)
 * 参考 xv6 实现
 *---------------------------------------------------------------------------*/
char *safestrcpy(char *dst, const char *src, size_t n)
{
    char *d = dst;
    if (n <= 0)
        return d;
    while (--n > 0 && (*d++ = *src++) != '\0')
        ;
    *d = '\0';
    return dst;
}

/*---------------------------------------------------------------------------
 * strcat — 字符串拼接
 *---------------------------------------------------------------------------*/
char *strcat(char *dst, const char *src)
{
    char *d = dst + strlen(dst);
    while ((*d++ = *src++) != '\0')
        ;
    return dst;
}

/*---------------------------------------------------------------------------
 * strchr — 在字符串中查找字符 (从前向后)
 *---------------------------------------------------------------------------*/
char *strchr(const char *s, int c)
{
    while (*s) {
        if (*s == (char)c)
            return (char *)s;
        s++;
    }
    if (c == '\0')
        return (char *)s;
    return NULL;
}

/*---------------------------------------------------------------------------
 * strrchr — 在字符串中查找字符 (从后向前)
 *---------------------------------------------------------------------------*/
char *strrchr(const char *s, int c)
{
    char *last = NULL;
    while (*s) {
        if (*s == (char)c)
            last = (char *)s;
        s++;
    }
    if (c == '\0')
        return (char *)s;
    return last;
}

/*---------------------------------------------------------------------------
 * uitoa — 无符号整数转字符串
 *---------------------------------------------------------------------------*/
char *uitoa(uint64_t n, char *buf, int base)
{
    static const char digits[] = "0123456789ABCDEF";
    char tmp[21];
    int  i = 0;

    if (base < 2 || base > 16) {
        buf[0] = '0';
        buf[1] = '\0';
        return buf;
    }

    if (n == 0) {
        buf[0] = '0';
        buf[1] = '\0';
        return buf;
    }

    while (n > 0) {
        tmp[i++] = digits[n % (uint64_t)base];
        n /= (uint64_t)base;
    }

    int j = 0;
    while (i > 0)
        buf[j++] = tmp[--i];
    buf[j] = '\0';

    return buf;
}

/*---------------------------------------------------------------------------
 * itoa — 有符号整数转十进制字符串
 *---------------------------------------------------------------------------*/
char *itoa(int64_t n, char *buf)
{
    if (n < 0) {
        buf[0] = '-';
        uitoa((uint64_t)(-n), buf + 1, 10);
    } else {
        uitoa((uint64_t)n, buf, 10);
    }
    return buf;
}

/*---------------------------------------------------------------------------
 * u64_to_hex — 格式化为 "0xXXXXXXXXXXXXXXXX"（18字节含结尾\0）
 *---------------------------------------------------------------------------*/
char *u64_to_hex(uint64_t n, char *buf)
{
    static const char hex[] = "0123456789ABCDEF";
    buf[0] = '0';
    buf[1] = 'x';
    for (int i = 0; i < 16; i++)
        buf[2 + i] = hex[(n >> (60 - i * 4)) & 0xF];
    buf[18] = '\0';
    return buf;
}