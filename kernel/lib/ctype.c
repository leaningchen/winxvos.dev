#include <types.h>
#include <ctype.h>

/*===========================================================================
 * ctype.c — 字符分类与转换函数实现
 *
 * 所有函数均基于 ASCII 码范围判断，不使用 locale，
 * 适合裸机内核环境（不依赖任何运行时库）。
 * 全部实现为单行表达式，编译器会将其内联优化。
 *===========================================================================*/

/*---------------------------------------------------------------------------
 * isdigit — 判断十进制数字: '0'(0x30) ~ '9'(0x39)
 *---------------------------------------------------------------------------*/
bool isdigit(int c) { return c >= '0' && c <= '9'; }

/*---------------------------------------------------------------------------
 * isalpha — 判断英文字母: 'a'~'z' 或 'A'~'Z'
 *---------------------------------------------------------------------------*/
bool isalpha(int c) { return (c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'); }

/*---------------------------------------------------------------------------
 * isalnum — 判断字母或数字: isalpha 或 isdigit 为真时返回 true
 *---------------------------------------------------------------------------*/
bool isalnum(int c) { return isalpha(c) || isdigit(c); }

/*---------------------------------------------------------------------------
 * isupper — 判断大写字母: 'A'(0x41) ~ 'Z'(0x5A)
 *---------------------------------------------------------------------------*/
bool isupper(int c) { return c >= 'A' && c <= 'Z'; }

/*---------------------------------------------------------------------------
 * islower — 判断小写字母: 'a'(0x61) ~ 'z'(0x7A)
 *---------------------------------------------------------------------------*/
bool islower(int c) { return c >= 'a' && c <= 'z'; }

/*---------------------------------------------------------------------------
 * isspace — 判断空白字符
 * 覆盖 C 标准定义的六种空白: ' '  '\t'  '\n'  '\r'  '\f'  '\v'
 *---------------------------------------------------------------------------*/
bool isspace(int c) { return c == ' ' || c == '\t' || c == '\n' || c == '\r' || c == '\f' || c == '\v'; }

/*---------------------------------------------------------------------------
 * isprint — 判断可打印字符（含空格）
 * ASCII 可打印范围: 0x20(' ') ~ 0x7E('~')，共 95 个字符
 *---------------------------------------------------------------------------*/
bool isprint(int c) { return c >= 0x20 && c <= 0x7E; }

/*---------------------------------------------------------------------------
 * toupper — 小写字母转大写
 * 大小写相差固定偏移 32（0x20），非小写字母原样返回。
 * 等价于: islower(c) ? c - 32 : c
 *---------------------------------------------------------------------------*/
int toupper(int c) { return islower(c) ? c - 'a' + 'A' : c; }

/*---------------------------------------------------------------------------
 * tolower — 大写字母转小写
 * 非大写字母原样返回。
 * 等价于: isupper(c) ? c + 32 : c
 *---------------------------------------------------------------------------*/
int tolower(int c) { return isupper(c) ? c - 'A' + 'a' : c; }
